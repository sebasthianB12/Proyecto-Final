def mostrar_cursos(categorias_elegidas, datos_cursos):
    for categoria in categorias_elegidas:
        categoria = categoria.strip()
        if categoria in datos_cursos:
            print(f"\n{categoria.capitalize()}:")
            for nombre_lista, cursos in datos_cursos[categoria].items():
                print(f"\n    - {nombre_lista}:\n")
                for i, nombre_curso in enumerate(cursos, start=1):
                    print(f"        {i}. {nombre_curso}")
        else:
            print(f"\nNo hay datos disponibles para la categoría {categoria.capitalize()}.\n")


